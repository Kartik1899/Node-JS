var a = [10,12,13];                     // Declaring array in js

a.push(14);                             // Inserts value at front
console.log(a);

a.pop();                                // Removes element from the end
console.log(a);

a.shift();                              // Removes element from front
console.log(a);

a.unshift(5);                           // Inserts element at front
console.log(a);